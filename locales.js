function buildMenuToCmd() {
  const map = {};
  const enMenu = MENUS.en.flat();
  for (const [lang, rows] of Object.entries(MENUS)) {
    if (lang === 'en') continue;
    const flat = rows.flat();
    for (let i = 0; i < flat.length && i < enMenu.length; i++) {
      map[flat[i]] = enMenu[i];
    }
  }
  return map;
}

